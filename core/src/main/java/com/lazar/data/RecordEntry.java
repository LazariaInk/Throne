package com.lazar.data;

public class RecordEntry {
    public String emperorName;
    public String yearsRuled;
    public String causeOfDeath;

    public RecordEntry() {
    }

    public RecordEntry(String emperorName, String yearsRuled, String causeOfDeath) {
        this.emperorName = emperorName;
        this.yearsRuled = yearsRuled;
        this.causeOfDeath = causeOfDeath;
    }
}
